#Operasi yang ditambahkan dengan variabel nilai (assignment)

a = 10 #assignment

a += 1 #a = a + 1
print("Nilai a: ", a)

a -= 2 #a = a - 2
print("Nilai a: ", a)

a *= 2 #a = a * 2
print("Nilai a: ", a)

a /= 2 #a = a : 2
print("Nilai a: ", a)

a %= 2 #a = a mod 2
print("Nilai a: ", a)

a //= 2 #a = a // 2
print("Nilai a: ", a)

a **=3 #a = a ** 3
print("Nilai a: ", a)

#Jika operator bitwise
c = True

c |= False
print("Nilai c: ", c)
