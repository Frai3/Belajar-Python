#Merubah tipe data dengan Casting

data_int = 10
print("Nilai: ", data_int)
print("Tipe: ", type(data_int))

data_float = float(data_int)
print("Nilai: ", data_float)
print("Tipe: ", type(data_float))

data_str = str(data_int)
print("Nilai: ", data_str)
print("Tipe: ", type(data_str))

#Bool jika nilai 0 maka False, selain itu akan True
data_bool = bool(data_int)
print("Nilai: ", data_bool)
print("Tipe: ", type(data_bool))