#Data yang dimasukkan pasti tipe string
data = input("Masukkan nilai: ")
print(data)

#Casting data ke int
data_int = int(data)
print(data_int)
#ATAU input langsung ke int
data = int(input("Masukkan nilai: "))
print(data)

#Data yang dimasukkan tipe boolean
biner = bool(int(input("Masukkan nilai: ")))
print(biner)
#Mengubah data menjadi int terlebih dahulu agar dapat diketahui True/False